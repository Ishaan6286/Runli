/* ── AI Recommendation Engine ────────────────────────── */
export function getAIRecommendations(userProfile) {
  const { goal, fitnessScore, activityLevel } = userProfile || {};

  // Score-based tags
  const scoreTags = [];
  if (fitnessScore) {
    if (fitnessScore < 40) scoreTags.push('beginner', 'recovery');
    else if (fitnessScore < 70) scoreTags.push('intermediate', 'recovery');
    else scoreTags.push('advanced', 'strength');
  } else {
    scoreTags.push('beginner');
  }

  // Goal-based tags
  const goalTags = [];
  if (goal === 'gain_muscle' || goal === 'muscle_gain') goalTags.push('muscle_gain', 'protein', 'creatine');
  else if (goal === 'lose_weight') goalTags.push('fat_loss', 'lean', 'bcaa');
  else goalTags.push('general', 'recovery');

  // Activity-based tags
  const activityTags = [];
  if (activityLevel === 'sedentary' || activityLevel === 'light') activityTags.push('beginner', 'home_gym');
  else if (activityLevel === 'very_active') activityTags.push('advanced', 'strength');

  const allTags = [...new Set([...scoreTags, ...goalTags, ...activityTags])];

  // Score each product
  const scored = PRODUCTS.map(p => {
    let score = 0;
    for (const tag of allTags) {
      if (p.tags.includes(tag)) score += 2;
    }
    if (p.goals && goal && p.goals.includes(goal)) score += 3;
    if (p.sections.includes('recommended')) score += 1;
    score += p.rating * 0.5;
    return { ...p, _score: score };
  });

  return scored
    .sort((a, b) => b._score - a._score)
    .slice(0, 6);
}

/* ── Analytics helper ────────────────────────────────── */
export function trackProductClick(productId, productName, destination) {
  try {
    const clicks = JSON.parse(localStorage.getItem('runli_product_clicks') || '[]');
    clicks.unshift({
      productId,
      productName,
      destination,
      timestamp: new Date().toISOString(),
    });
    localStorage.setItem('runli_product_clicks', JSON.stringify(clicks.slice(0, 100)));
  } catch {}
}
